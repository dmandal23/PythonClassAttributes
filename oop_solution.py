class Mobile:
    def __init__(self, userbrand, usermodel):
        self.brand = userbrand 
        self.model = usermodel

      


my_mobile = Mobile("Vivo","Vivo T3")

print(my_mobile.brand)
print(my_mobile.model)


add_mobile = Mobile("Samsung","Galaxy A16")

print(add_mobile.brand)
print(add_mobile.model)

